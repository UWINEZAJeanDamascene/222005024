public class ifelseifladder {
    public static void main(String[] args) {
        int no=10;

        if(no>8){
            System.out.println("positive");
        }
        else if(no<6){
            System.out.println("somehow good");
        }
        else if(no==10){
            System.out.println("extremely positive");
        }
        else{
            System.out.println("wrong results");
        }
    }
}
